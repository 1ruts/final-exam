function displayFaculty(faculty) {
    const facultyTable = document.getElementById('facultyTable');
    facultyTable.innerHTML = '';
    faculty.forEach(member => {
        const row = document.createElement('tr');
        row.innerHTML = `
            <td>${member.name}</td>
            <td>${member.title}</td>
            <td><a href="${member.homepage}">Link</a></td>
            <td>${member.email}</td>
            <td>${member.phone}</td>
            <td>${member.room}</td>
            <td>${member.officeHours}</td>
        `;
        facultyTable.appendChild(row);
    });
}


